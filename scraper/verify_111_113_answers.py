all_official = {111: official_111, 112: official_112, 113: official_113}

# Subject-to-question-number mapping for each paper (need to determine from data)
for year in [111, 112, 113]:
    data = json.load(open(f'data/{year}_complete.json', 'r', encoding='utf-8'))
    official = all_official[year]
    
    total_checked = 0
    total_mismatch = 0
    mismatch_details = []
    
    for q in data:
        qid = q['id']
        paper = qid.split('-')[1]  # e.g. '2301'
        qnum = q.get('questionNumber', 0)
        db_ans = q.get('answer', '?')
        
        if paper in official and qnum in official[paper]:
            correct = official[paper][qnum]
            total_checked += 1
            if db_ans != correct:
                total_mismatch += 1
                mismatch_details.append(f"  {qid}: DB={db_ans} -> should be {correct}")
    
    print(f"\n{'='*50}")
    print(f"  {year}年: {total_mismatch}/{total_checked} answers WRONG")
    print(f"{'='*50}")
    if mismatch_details:
        for d in mismatch_details:
            print(d)
    else:
        print("  ALL CORRECT!")

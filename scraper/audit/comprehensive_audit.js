async function run() {
  console.log('📊 全面盤點：官方答案 vs Firestore 題數 vs 詳解\n');
  
  const result = {};
  
  for (const year of [111, 112, 113, 114]) {
    const snap = await db.collection('questions').where('year', '==', year).get();
    
    // Group by paper
    const byPaper = {};
    const bySubject = {};
    
    snap.forEach(doc => {
      const d = doc.data();
      const parts = doc.id.split('-');
      const paper = parts[1];
      
      if (!byPaper[paper]) byPaper[paper] = { total: 0, withExp: 0, noExp: 0, ansWrong: 0, questions: [] };
      byPaper[paper].total++;
      
      const hasExp = d.explanation && typeof d.explanation === 'object' && d.explanation.coreConcept;
      if (hasExp) byPaper[paper].withExp++;
      else byPaper[paper].noExp++;
      
      const officialAns = official[year]?.[paper]?.[d.questionNumber];
      if (officialAns && d.answer !== officialAns) byPaper[paper].ansWrong++;
      
      byPaper[paper].questions.push(d.questionNumber);
      
      // By subject
      const subj = d.subject || 'unknown';
      if (!bySubject[subj]) bySubject[subj] = { total: 0, withExp: 0, noExp: 0 };
      bySubject[subj].total++;
      if (hasExp) bySubject[subj].withExp++;
      else bySubject[subj].noExp++;
    });
    
    console.log(`\n${'='.repeat(70)}`);
    console.log(`${year}年 — Firestore 總題數: ${snap.size}`);
    console.log('='.repeat(70));
    
    // Per paper comparison
    console.log('\n  【試卷別】');
    for (const [paper, data] of Object.entries(byPaper).sort()) {
      const expected = EXPECTED[paper] || '?';
      const qnums = data.questions.sort((a,b) => a - b);
      const min = qnums[0];
      const max = qnums[qnums.length - 1];
      
      // Find missing question numbers
      const missing = [];
      for (let i = 1; i <= expected; i++) {
        if (!qnums.includes(i)) missing.push(i);
      }
      
      // Find extra question numbers
      const extra = qnums.filter(n => n > expected);
      
      const status = data.total === expected ? '✅' : '❌';
      console.log(`  ${paper}: 官方${expected}題 / DB${data.total}題 ${status} | 有詳解${data.withExp} 無詳解${data.noExp} | 答案錯${data.ansWrong}`);
      
      if (missing.length > 0 && missing.length <= 10) {
        console.log(`    ⚠️ 缺少題號: ${missing.join(', ')}`);
      } else if (missing.length > 10) {
        console.log(`    ⚠️ 缺少 ${missing.length} 題 (${missing.slice(0, 5).join(',')}...)`);
      }
      if (extra.length > 0) {
        console.log(`    ⚠️ 多出題號: ${extra.join(', ')}`);
      }
    }
    
    // Per subject breakdown
    console.log('\n  【科目別】');
    for (const [subj, data] of Object.entries(bySubject).sort()) {
      console.log(`  ${subj.padEnd(25)}: ${String(data.total).padStart(3)}題 | 有詳解${String(data.withExp).padStart(3)} 無詳解${String(data.noExp).padStart(3)}`);
    }
    
    // Official answer count
    const officialPapers = official[year] || {};
    console.log('\n  【官方答案數量】');
    for (const [paper, answers] of Object.entries(officialPapers).sort()) {
      const ansCount = Object.keys(answers).length;
      console.log(`  ${paper}: ${ansCount} 個答案`);
    }
    
    result[year] = { firestore: snap.size, byPaper, bySubject };
  }
  
  console.log('\n\n' + '='.repeat(70));
  console.log('彙總');
  console.log('='.repeat(70));
  for (const [year, data] of Object.entries(result)) {
    console.log(`  ${year}年: Firestore ${data.firestore}題, 期望 300題 ${data.firestore === 300 ? '✅' : '❌'}`);
  }
  
  process.exit();
}

run();
